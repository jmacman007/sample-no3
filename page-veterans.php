			<h2>How Pension Is Calculated</h2>
			<p>The monthly award is based on VA totaling 12 months of estimated future income and subtracting from that 12 months of estimated future, recurring and predictable medical expenses. Allowable medical expenses are reduced by a deductible to produce an adjusted medical expense which in turn is subtracted from the estimated 12 months of future income.</p>
			<p>The new income derived from subtracting adjusted medical expenses from income is called "countable" income or IVAP (Income for Veterans Affairs Purposes). This countable income is then subtracted from the Maximum Allowable Pension Rate -- MAPR -- and that result is divided by 12 to determine the monthly income Pension award. This award is paid in addition to the family income that already exists. See examples below.</p>
