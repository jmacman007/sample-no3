			<h2>Annualizing Costs of Assisted living, Residential Care, Adult Day Care or Other Similar Arrangements</h2>
			<p>These facilities are not categorized by VA as nursing homes. As such, annualization of costs and a rating are not automatic. If the beneficiary is not rated, the service representative will only allow recurring unreimbursed medical expenses for specific medical care provided by licensed health professionals. Costs for room and board or custodial care cannot be applied.</p>
			<p>For information on ratings please go to the article entitled "Who is eligible for the aid and attendance Pension benefit?".</p>
			<p>On the other hand, if a beneficiary residing in one of these living arrangements has been rated a need for "aid and attendance" or "housebound", VA will allow all reasonable costs to be counted as prospective, annualized medical expenses as long as some of those costs are paid for medical care. The providers do not have to be licensed. In the case of Alzheimer's, the physician's statement used for rating must indicate the person needing care must be in a protective environment; otherwise, only medical costs are covered. Applying for a rating is discussed in a previous section above. All reasonable costs would include room and board as well as other unreimbursed billable services.</p>
			<p>For an explanation of the special annualized treatment of unreimbursed long term care costs and insurance premiums please go to the article entitled "Understanding the special case of long term care medical costs".</p>
			<p>The director of the facility must sign a statement verifying the type of care being given and the fact that the person receiving the care is expected to remain a resident in the facility. We have included a form entitled "Care Provider Report (used to provide evidence of recurring medical expenses)" We highly recommend this form or a similar form be submitted with the application. A copy of the contract for services as well as invoices and statements from the facility should also be included with that Form.</p>
			<p>There may be a possibility of a non-veteran spouse of a living veteran receiving annualized credit for recurring costs of non-nursing home facility care.  VA will allow deduction of non-medical costs in a facility if the resident's doctor produces a letter that says the person needing care must be in a "protected environment."</p>
			<p>VA will not allow a Pension rating for a non-veteran spouse of a living veteran so the benefit will be a lesser amount. If VA allows annualization of facility costs for a spouse of a living veteran, there will be no Pension allowance for aid and attendance or housebound, and the Pension award will be much smaller.</p>
			<p>Of course, a death claim is different because the surviving spouse can receive a rating in that case.</p>

