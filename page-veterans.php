			<h2>The Rating</h2>
			<p>A rating for "aid and attendance" or "housebound" allows VA to pay additional benefits beyond the regular Pension benefit ceiling in order to help cover the additional costs associated with added disabilities. A rating for these allowances is determined by a veteran service representative who has been trained to recognize from medical reports and interviews whether the veteran or his surviving spouse needs the additional care.</p>
			<p>Determinations of a need for aid and attendance or housebound benefits may be based on medical reports and findings by private physicians or from hospital facilities. Authorization of aid and attendance benefits without a rating decision is automatic if evidence establishes the claimant is a patient in a nursing home. Aid and attendance is also automatic if the claimant is blind or nearly blind or having severe visual problems.</p>
			<p>According to 38 CFR Part Three, the following criteria are used to determine the need for aid and attendance:</p>
			<ul>
				<li>Inability of claimant to dress or undress himself (herself), or to keep himself (herself) ordinarily clean and presentable;</li>
				<li>Frequent need of adjustment of any special prosthetic or orthopedic appliances which by reason of the particular disability cannot be done without aid (this will not include the adjustment of appliances which normal persons would be unable to adjust without aid, such as supports, belts, lacing at the back, etc.);</li>
				<li>Inability of claimant to feed himself (herself) through loss of coordination of upper extremities or through extreme weakness;</li>
				<li>Inability to attend to the wants of nature;</li>
				<li>Or incapacity, physical or mental, which requires care or assistance on a regular basis to protect the claimant from hazards or dangers incident to his or her daily environment.</li>
			</ul>
			<p>Not all of the disabling conditions in the list above are required to exist before a favorable rating may be made. The personal functions which the veteran is unable to perform are considered in connection with his or her condition as a whole. It is only necessary that the evidence establish that the veteran is so helpless as to need "regular" (scheduled and ongoing) aid and attendance from someone else, not that there be a 24-hour need.</p>
			<p>"Bedridden" is a definition that allows a rating for aid and attendance by itself. "Bedridden" is a condition which requires that the claimant remain in bed. A person who has voluntarily taken to bed or who has been told by the doctor to remain in bed will not necessarily receive the favorable rating for aid and attendance. There must be an actual need for personal assistance from others.</p>
			<p>Housebound means "permanently housebound by reason of disability or disabilities." This requirement is met when the veteran or his or her widow is substantially confined to his or her dwelling and the immediate premises or, if institutionalized, to the ward or clinical area, and it is reasonably certain that the disability or disabilities and resultant confinement will continue throughout his or her lifetime.</p>
			<p>A person who cannot leave his immediate premises unless under the supervision of another person is considered housebound. This might include the inability to drive because of the disability.</p>
			<p>A housebound rating does not mean a person needs to be confined to a personal residence. It can apply to any place where the person is living whether in a facility or in the home of someone else.</p>
			<p>In order to receive one of these ratings the claimant must schedule an exam with his or her physician and have the physician complete VA Form 21-2680. This Examination for Housebound Status or Permanent need for Regular Aid and Attendance is then included with the initial application. We also provide in our book a supplemental form entitled "Form 12 -- Statement of Attending Physician (used to determine rating for A&A or HB)." This document is similar to a form used internally by VA to obtain information from veterans medical facilities for determining a rating. It is in a format that a veterans service representative would recognize.</p>
			<p>Medical evidence for a rating for "aid and attendance" or "housebound" for living arrangements other than a nursing home should be submitted with the application to avoid a delay in the approval process. Waiting for the regional office to order medical records is a time-consuming process, mainly because doctors offices don't respond quickly to these kinds of requests.</p>
