			<h2>The Special Case for Long Term Care Costs</h2>
			<p>A special provision for calculating Pension income, allows household income to be reduced by 12 months worth of future, recurring medical expenses. Normally, income is only reduced by medical expenses incurred in the month of application. These allowable, annualized medical expenses are such things as insurance premiums, the cost of home care, the cost of paying any person to provide care, the cost of adult day care, the cost of assisted living and the cost of a nursing home facility. In most cases, these expenses are only deductible if there is a rating.</p>
			<p>This special provision can allow veteran households earning more than the annual MAPR to qualify for Pension. As an example, a veteran household earning $6,000 a month could still qualify for Pension if the veteran is paying $4,500 to $6,000 a month for nursing home costs. The applicant must submit appropriate evidence for a rating and for recurring costs in order to qualify for this special provision. VA normally does not tell applicants about this special treatment of medical expenses or how to qualify for it.</p>
			<p>For an explanation of the special annualized treatment of unreimbursed long term care costs and insurance premiums please go to the article entitled "Understanding the special case of long term care medical costs".</p>
