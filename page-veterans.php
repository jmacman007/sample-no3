			<h2>Dealing with Assets That May Disqualify the Applicant</h2>
			<p>There is also an asset test to qualify for Pension. Any asset or investment that could be easily converted into income might disqualify the claimant. An asset ceiling of $80,000 is often cited in the media as being the test. The $80,000 has to do with VA internal filing requirements and is not an actual test. In reality, there is no dollar amount for the test and any level of assets could block the award. The asset test ultimately becomes a subjective decision made by the veterans service representative, processing the application.</p>
			<p>A home, used as a residence, vehicles and difficult-to-sell property are generally excluded from the asset test.</p>
